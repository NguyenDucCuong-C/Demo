    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String password = String.valueOf(pswLLogin.getPassword());

        StringBuilder favourite = new StringBuilder();
        if (ckGame.isSelected()) {
            favourite.append(ckGame.getText()).append(" ");
        }
        if (ckNhac.isSelected()) {
            favourite.append(ckNhac.getText()).append(" ");
        }

        String gender = "";
        if (rdoMale.isSelected()) {
            gender = rdoMale.getText();
        }
        if (rdoFemale.isSelected()) {
            gender = rdoFemale.getText();
        }

        lblmatkhau.setText("Mat khau la: " + password + "  \nSo thich: " + favourite + "   \nGender: " + gender);
    }//GEN-LAST:event_btnLoginActionPerformed

